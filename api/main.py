# =====================================================
# CREATE FASTAPI APP
# =====================================================
app = FastAPI()


# =====================================================
# PREDICT API
# =====================================================
@app.post("/predict")
def predict(
    files_changed: int,
    total_changes: int,
    ratio: float,
    large_change: int,
    multi_file: int,
    merge: int
):

    prob, level = predict_risk(
        files_changed,
        total_changes,
        ratio,
        large_change,
        multi_file,
        merge
    )

    reasons = explain_risk({
        "files_changed": files_changed,
        "total_changes": total_changes,
        "multi_file_change": multi_file,
        "merge_activity": merge
    })

    return {
        "conflict_probability": float(prob),
        "risk_level": level,
        "reasons": reasons
    }


# =====================================================
# GITHUB WEBHOOK
# =====================================================
@app.post("/github-webhook")
async def github_webhook(request: Request):

    payload = await request.json()

    repo_name = payload.get("repository", {}).get("full_name", "unknown/repo")
    commits = payload.get("commits", [])

    # ---------- FEATURE EXTRACTION ----------
    files_changed = len(commits)

    total_changes = sum(
        len(c.get("added", [])) +
        len(c.get("removed", []))
        for c in commits
    )

    ratio = 1.0
    large_change = 1 if total_changes > 200 else 0
    multi_file = 1 if files_changed > 3 else 0
    merge = 1

    # ---------- PREDICTION ----------
    prob, level = predict_risk(
        files_changed,
        total_changes,
        ratio,
        large_change,
        multi_file,
        merge
    )

    # ---------- EXPLAINABILITY ----------
    reasons = explain_risk({
        "files_changed": files_changed,
        "total_changes": total_changes,
        "multi_file_change": multi_file,
        "merge_activity": merge
    })

    reason_text = (
        "\n".join([f"- {r}" for r in reasons])
        if reasons else "- No strong risk signals detected"
    )

    # ---------- RISKY AREAS ----------
    try:
        risky_areas = detect_risky_lines(commits)
    except Exception as e:
        print("Risk detection error:", e)
        risky_areas = []

    risk_area_text = ""
    for r in risky_areas:
        risk_area_text += f"\n- {r.get('file')} → {r.get('reason')}"

    if not risk_area_text:
        risk_area_text = "\n- No risky files detected"

    # ---------- AUTO RESOLVER ----------
    auto_fix_message = ""

    if level == "HIGH":
        try:
            sample_code = "print('hello')    \nprint('world')    "
            cleaned_code = resolve_whitespace(sample_code)

            auto_fix_message = f"""
---
### ⚡ Auto Resolver Triggered
Whitespace cleanup applied automatically.

Preview:
"""

            print("Auto-fixed preview:\n", cleaned_code)

        except Exception as e:
            print("Auto resolver failed:", e)

    # ---------- COMMENT MESSAGE ----------
    message = f"""
🤖 **AI Merge Conflict Analysis**

### 🔥 Risk Level: **{level}**
### 📊 Probability: **{prob:.2f}**

---

### ⚠️ Why risk is {level}:
{reason_text}

---

### 📂 Risky Areas
{risk_area_text}

---

### 📈 Signals
- Files Changed: {files_changed}
- Total Changes: {total_changes}
- Large Change: {large_change}
- Multi File Commit: {multi_file}

{auto_fix_message}

⚡ Generated by **Merge Conflict Predictor AI**
"""

    # ---------- POST COMMENTS ----------
    for commit in commits:
        sha = commit["id"]
        post_commit_comment(repo_name, sha, message)

    return {
        "status": "analyzed",
        "risk_level": level,
        "probability": float(prob),
        "reasons": reasons,
        "risky_areas": risky_areas
    }


